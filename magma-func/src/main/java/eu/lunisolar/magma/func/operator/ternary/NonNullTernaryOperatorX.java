/**
 * Specialization that makes sure that returned value is always non null.
 *
 * This class is not functional interface itself to avoid adding another level of ambiguity for the compiler.
 */
public final class NonNullTernaryOperatorX<T, X extends Exception> implements TernaryOperatorX<T, X> {

	private final TernaryOperatorX<T, X> anyImplementation;

	public NonNullTernaryOperatorX(@Nonnull TernaryOperatorX<T, X> anyImplementation) {
		Objects.requireNonNull(anyImplementation, Function4U.VALIDATION_MESSAGE_ANYIMPLEMENTATION);
		this.anyImplementation = anyImplementation;
	}

	@Nonnull
	public final T apply(T t1, T t2, T t3) throws X {
		return Objects.requireNonNull(anyImplementation.apply(t1, t2, t3));
	}

	@Override
	@Nonnull
	public final T nonNull(T t1, T t2, T t3) throws X {
		return apply(t1, t2, t3);
	}
}
