/**
 * Specialization that makes sure that returned value is always non null.
 *
 * This class is not functional interface itself to avoid adding another level of ambiguity for the compiler.
 */
public final class NonNullBooleanBiFunctionX<R, X extends Exception> implements BooleanBiFunctionX<R, X> {

	private final BooleanBiFunctionX<R, X> anyImplementation;

	public NonNullBooleanBiFunctionX(@Nonnull BooleanBiFunctionX<R, X> anyImplementation) {
		Objects.requireNonNull(anyImplementation, Function4U.VALIDATION_MESSAGE_ANYIMPLEMENTATION);
		this.anyImplementation = anyImplementation;
	}

	@Nonnull
	public final R apply(boolean b1, boolean b2) throws X {
		return Objects.requireNonNull(anyImplementation.apply(b1, b2));
	}

	@Override
	@Nonnull
	public final R nonNull(boolean b1, boolean b2) throws X {
		return apply(b1, b2);
	}
}
