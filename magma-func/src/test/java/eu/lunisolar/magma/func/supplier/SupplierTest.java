    public void testNonNullCapturesNull() throws ParseException {
        sutNull.nonNull();
    }


    @Test
    public void testFunctionalInterfaceDescription() throws ParseException {
        assertThat(sut.functionalInterfaceDescription())
            .isEqualTo("Supplier: R get()");
    }

