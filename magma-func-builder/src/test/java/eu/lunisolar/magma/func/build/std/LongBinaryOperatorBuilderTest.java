import static org.assertj.core.api.Assertions.*; //NOSONAR

public class LongBinaryOperatorBuilderTest{

    @SuppressWarnings("unchecked")
    public static final DefaultFunctionalAssertions<ObjectAssert> A = new DefaultFunctionalAssertions() {
    };

    @Test
    public void testEventuallyThrow()  {

        assertThatThrownBy(() -> {
            LongBinaryOperator function = longBinaryOperatorFrom(b-> b
                .build()
            );

            function.applyAsLong(100L,100L);

            fail("No exception were thrown.");
        })
                    .isExactlyInstanceOf(IllegalStateException.class)
                    .hasMessageContaining("There is no case configured for the arguments (if any).");
    }

    @Test
    public void testHandlingCanBeSetOnlyOnce()  {


        assertThatThrownBy(() -> {
            LongBinaryOperator function = longBinaryOperatorFrom(b-> b
                .withHandling(h -> h.wrapWhen(p -> p.isRuntime(), RuntimeException::new))
                .build(h -> h.wrapWhen(p -> p.isRuntime(), RuntimeException::new))
            );

            fail("No exception were thrown.");
        })
                    .isExactlyInstanceOf(UnsupportedOperationException.class)
                    .hasMessageContaining("Handling is already set for this builder.");
    }

    @Test
    public void testHandling()  {

        assertThatThrownBy(() -> {
            LongBinaryOperator function = longBinaryOperatorFrom(b -> b
                .eventually((a1,a2) -> {
                        throw new RuntimeException("ORIGINAL");
                    })
                .build(h -> h.wrapWhen(p -> p.isRuntime(),  IllegalStateException::new, "NEW EXCEPTION"))
            );

            function.applyAsLong(100L,100L);

            fail("No exception were thrown.");
        })
                    .isExactlyInstanceOf(IllegalStateException.class)
                    .hasMessageContaining("NEW EXCEPTION")
                    .hasCauseExactlyInstanceOf(RuntimeException.class);
    }


    @Test
    public void testBuild()  {

        LongBinaryOperator function = longBinaryOperatorFrom( b -> b
            .aCase(ce -> ce.of((a1,a2) -> a1 == 0L)
                             .evaluate((a1,a2) -> 0L))
            .inCase((a1,a2) -> a1 > 0L && a1 < 10L).evaluate((a1,a2) -> 1L)
            .inCase((a1,a2) -> a1 > 10L && a1 < 20L).evaluate((a1,a2) -> 2L)
            .eventually((a1,a2) -> 99L)
            .build()
        );


        A.assertLongBinaryOp(function)
            .doesApplyAsLong(0L,0L).when(null).to(a -> a.isEqualTo(0L))
            .doesApplyAsLong(5L,5L).when(null).to(a -> a.isEqualTo(1L))
            .doesApplyAsLong(15L,15L).when(null).to(a -> a.isEqualTo(2L))
            .doesApplyAsLong(10L,10L).when(null).to(a -> a.isEqualTo(99L))
        ;

    }

}
